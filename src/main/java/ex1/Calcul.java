package ex1;

public class Calcul {

	/**
	 * Ajoute deux entiers et renvoie le résultat.
	 * @param a le premier entier
	 * @param b le deuxième entier
	 * @return la somme de a et b
	 */
	public int additionner(int a, int b) {
		return a + b;
	}

	/**
	 * Soustrait le deuxième entier du premier et renvoie le résultat.
	 * @param a le premier entier
	 * @param b le deuxième entier
	 * @return le résultat de a - b
	 */
	public int soustraire(int a, int b) {
		return a - b;
	}
}
